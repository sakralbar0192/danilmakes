/**
 * Безопасный доступ к карте CSS Modules (`<style module>`) для компонентов,
 * которые рендерятся внутри виртуализированного списка тарифов: при редком
 * отсутствии инъекции `$style` на инстансе не падает шаблон.
 */
export default {
  computed: {
    vmCss() {
      return this.$style || {};
    },
  },
};
